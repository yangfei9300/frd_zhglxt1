        <template slot-scope="scope">
          <el-button
            size="mini"
            type="text"
            icon="el-icon-download"
            @click="downloadfilie(scope.row)"
            v-hasPermi="['exhibitor:exhibitor_file:edit']"
          >下载</el-button>
